// За два броја (а и б) исписати њихов: Збир,разлику,производ,количник,остатак при дељењу,степен (а^b)

let a = 5
let b = 3

let zbir = a + b
console.log('Zbir brojeva je =', zbir)

let razlika = a - b
console.log('Razlika brojeva je =', razlika)

let proizvod = a * b
console.log('Prozivod brojeva je =', proizvod)

let kolicnik = a / b
console.log('Kolicnik brojeva je =', kolicnik)

let ostatak = a % b
console.log('Ostatak pri deljenju je =', ostatak)

let stepen = a ** b
console.log('Stepen brojeva je =', stepen)